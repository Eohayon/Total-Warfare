public class Enums {


    /**
     * Enum for player number
     * @author  Ethan Ohayon
     */
    public enum Player {
        ONE, TWO
    }

    /**
     * Enum for rocket types
     * @author  Ethan Ohayon
     */
    public enum Rocket {
        CHAIM, TRACTOR
    }

    /**
     * Enum for tank types
     * @author  Ethan Ohayon
     */
    public enum Tank {
        DESERT, GREEN, GREY, NAVY
    }

    /**
     * Enum for barrel type
     * @author  Ethan Ohayon
     */
    public enum Barrel {
        GREEN, GREY, RED
    }

    /**
     * Enum for driving directions
     * @author  Ethan Ohayon
     */
    public enum Direction {
        LEFT, RIGHT
    }
}
